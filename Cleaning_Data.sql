-------- replace nulls with mode --------
  CREATE TEMP TABLE modes AS
    SELECT RACE, 
    PREG,
    NOPRIOR
    FROM remove_Nulls
    WHERE RACE !=-9
    and PREG != -9
    and NOPRIOR != -9
    GROUP BY RACE, 
    PREG,
    NOPRIOR
    ORDER BY COUNT(*) DESC
    LIMIT 1;


SELECT * from modes;

---JOIN TABLES
CREATE TEMP TABLE replaced_Nulls AS
 SELECT
  c.CASEID,
  CASE WHEN c.RACE = -9 THEN m.race ELSE c.RACE END AS RACE,

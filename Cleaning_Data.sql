--BINARIZATION

CREATE TEMP TABLE binarized AS
SELECT 
  CASEID,
  RACE,
  AGE,
  FRSTUSE1,
  CASE WHEN PREG = 2 THEN 0 ELSE 1 END AS PREG,
  NOPRIOR,
  DSMCRIT,
  CASE WHEN PSYPROB = 2 THEN 0 ELSE 1 END AS PSYPROB,
  SERVICES,
  CASE 
    WHEN LOS <=30 THEN 0
    WHEN LOS IN (31, 32) THEN 1
    WHEN LOS IN (33,34,35) THEN 2
    WHEN LOS IN (36,37) THEN 3
  END AS LOS_PERIOD,
  REASON,
  SUB1,
  FREQ1,
  ROUTE1,
  FREQ_ATND_SELF_HELP,
  CASE WHEN METHUSE = 2 THEN 0 ELSE 1 END AS METHUSE,
  BENZFLG,
  TRNQFLG,
  IDU
FROM replaced_Nulls;

--- < 30 = 1-30 days
--- 31,32 = 31-60 days
--- 33-35 = 61-180 days
--- 36-37 = 181-365+ days

select * from binarized limit 10; 


CREATE TABLE teds.final AS
  SELECT * EXCEPT (METHUSE, BENZFLG, TRNQFLG, IDU, LOS_PERIOD), 
  CASE WHEN METHUSE = 1 OR
  BENZFLG = 1 OR
  TRNQFLG = 1 OR
  IDU = 1
  THEN 1 ELSE 0 END AS HIGH_RISK
  FROM binarized;

CREATE TABLE teds.final_los AS
  SELECT * 
  FROM binarized;
